#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char	*ptr;

	ptr = s1;
	ptr += ft_strlen(s1);
	while (n-- && *s2)
		*(ptr++) = *(s2++);
	*ptr = '\0';
	return (s1);
}
